constexpr int P = 998244353;
using Z = MInt<P>;

void solve() {
    int n = read();
    vector<int> a(n);
    for (auto &x : a) x = read();

    array<Z, 2> dp;
    dp[0] = 1;

    for (int i = 0; i < n; i++) {
        array<Z, 2> ndp;
        
        for (int x : {0, 1}) {
            int l;
            if (i == 0) {
                l = 0;
            } else if (x == 0) {
                l = a[i - 1];
            } else if (i == 1) {
                l = 1;
            } else {
                l = a[i - 2] + 1;
            }

            for (int y : {0, 1}) {
                if (y == 0 and a[i] != l) continue;
                if (x == 1 and y == 1) continue;

                ndp[y] += dp[x];
            }
        }

        dp = ndp;
    }

    cout << dp[0] + dp[1] << '\n';
}

