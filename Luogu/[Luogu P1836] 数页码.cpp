int read()
{
    int f = 1, x = 0;
    char ch = getchar();
    while (ch < '0' or ch > '9')
        f = ch == '-' ? -1 : 1, ch = getchar();
    while (ch >= '0' and ch <= '9')
        x = x * 10 + ch - '0', ch = getchar();
    return x * f;
}

int cnt(int x)
{
    int res = 0;
    while (x % 10 == 9)
        res++, x /= 10;
    return res;
}

