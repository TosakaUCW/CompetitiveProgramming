using Z = MInt<P>;

using mat = array<array<Z, 3>, 3>;
mat operator * (mat a, mat b) {
    mat c;
    for (int i = 0; i < 3; i++) {
        for (int j = 0; j < 3; j++) {
            for (int k = 0; k < 3; k++) {
                c[i][j] += a[i][k] * b[k][j];
            }
        }
    }
    return c;
};

void solve() {
    int n = read();
    int m = read();

    Z C = power(Z(2), m);
    Z x = C - 1;
    vector<Z> f(5);
    f[1] = 1;
    f[2] = 0;
    for (int i = 3; i <= 4; i++) {
        f[i] = C * power(x, i - 2) - f[i - 2] * x;
    }

    if (n <= 4) {
        cout << f[n] << '\n';
        return;
    }

    mat coef {
        array<Z, 3> {-x, 0, 1},
        array<Z, 3> {1, 0, 0},
        array<Z, 3> {0, 0, x * x}
    };
    coef = power(coef, (n - 1) / 2 - 1);

    mat u;
    if (n & 1) {
        u = {
           array<Z, 3> {0, 0, f[3]},
           array<Z, 3> {0, 0, f[1]},
           array<Z, 3> {0, 0, C * power(x, 3)} 
        };
    } else {
        u = {
           array<Z, 3> {0, 0, f[4]},
           array<Z, 3> {0, 0, f[2]},
           array<Z, 3> {0, 0, C * power(x, 4)}
        };
    }

    u = coef * u;

    cout << u[0][2] << '\n';
}

