void solve() {
    // int n; cin >> n;

    vector<int> a(3);
    int n;
    cin >> n >> a[0] >> a[1] >> a[2];

    sort(a.begin(), a.end());

    int ans = n;
    for (int i = 0; i <= 1; i++) {
        for (int j = 0; j <= 1; j++) {
            for (int k = 0; k <= 1; k++) {
                int x = n;
                if (i) x -= a[0];
                if (j) x -= a[1];
                if (k) x -= a[2];

                if (x < 0) continue;
                ans = min(ans, x);
            }
        }
    }

    cout << ans << '\n';
}

